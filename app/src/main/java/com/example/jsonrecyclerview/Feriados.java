package com.example.jsonrecyclerview;

public class Feriados {



    private  final  String name;
    private  final String date;

    public Feriados(String name, String date){


        this.name = name;
        this.date = date;
    }
    public String getName() {
        return name;
    }

    public String getDate() {
        return date;
    }
}


